class Produto
{
    private string nome;
    private int id;
    private double valor;
    private string fornecedor;
    public string Nome
    {
        get { return nome; }
        set { nome = value; }
    }
    public int Id
    {
        get { return id; }
        set { id = value; }
    }
    public double Valor
    {
        get { return valor; }
        set { valor = value; }
    }
     public string Fornecedor
    {
        get { return fornecedor; }
        set { fornecedor = value; }
    }
}



